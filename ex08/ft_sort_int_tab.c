void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	m;

	i = 0;
	while (i < size)
	{
		j = i;
		m = i;
		while (j < size)
			if (tab[j++] < tab[m])
				m = j - 1;
		j = tab[i];
		tab[i] = tab[m];
		tab[m] = j;
		i++;
	}
}
